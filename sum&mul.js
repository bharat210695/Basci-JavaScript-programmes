//Find sum and multiplication of  all natural numbers from 10 to 20

//sum
let sum = 0
for (let i = 10; i <= 20; i++) {
    sum = sum + i;
}
console.log(sum);

//multiplication
let mul = 0
for (let i = 10; i <= 20; i++) {
    mul = mul * i;
}
console.log(mul);

//output
// sum of number 10 to 20
165
